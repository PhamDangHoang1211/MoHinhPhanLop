    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            // TODO add your handling code here:
            String name=txtName.getText();
            String status=boxStatus.getSelectedItem().toString();      
            myConnectUnit unit=new myConnectUnit();
            if(status.equals("Active"))
            {
                status="1";
            }
            else status="0";
            HashMap<String,Object> valueInsert=new HashMap<String,Object>();
            valueInsert.put("categories_name", name);
            valueInsert.put("status",status);
            unit.Insert("categories", valueInsert);
            JOptionPane.showMessageDialog(null, "Add Succecced !!!");
            tableUpdate();
        } catch (Exception ex) {
            Logger.getLogger(AdminMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

