    private void tableUpdate() throws Exception{
        String temp;
        categoryDAL cate=new categoryDAL();
        ArrayList<categoryDTO> abc=new ArrayList<>();
        abc=cate.getAllCategories();      
        DefaultTableModel d=(DefaultTableModel) tabCategories.getModel();
        d.setRowCount(0);           
                for(int i=0;i<abc.size();i++)
                {
                    Vector v2=new Vector();
                    v2.add(abc.get(i).getId());
                    v2.add(abc.get(i).getCate_name());
                    if(abc.get(i).getStatus()==1)
                    {
                        temp="Active";
                    }
                    else temp="Deactive";
                    v2.add(temp);
                    d.addRow(v2);
                    System.out.println(v2);
                }
         
    }
