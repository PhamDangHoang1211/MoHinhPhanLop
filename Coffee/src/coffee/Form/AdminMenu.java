        // TODO add your handling code here:
        DefaultTableModel d=(DefaultTableModel) tabCategories.getModel();
        int selectIndex=tabCategories.getSelectedRow();
        
        String id=(d.getValueAt(selectIndex, 0).toString());
        String condition=" id= "+id;
        System.out.println(condition);
        String status=boxStatus.getSelectedItem().toString();
        String CategoryName=txtName.getText();
        try {
            MySQLConnect connect=new MySQLConnect();
            myConnectUnit unit=new myConnectUnit();
            if(status.equals("Active"))
            {
                status="1";
            }
            else status="0";
            HashMap<String,Object> valueInsert=new HashMap<String,Object>();
            valueInsert.put("categories_name", CategoryName);
            valueInsert.put("status",status);
            unit.Update("categories", valueInsert,condition);
            System.out.println("Cập nhật thành công");
            tableUpdate();
            connect.Close();
            txtName.requestFocus();
        } catch (Exception e) {
        }
        
